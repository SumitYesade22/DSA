class Solution:
    def integerBreak(self, n: int) -> int:
        dp=[-1]*(n+1)
        ans=self.solve(n,dp)
        return ans
    def solve(self,n,dp):
        if n==1:
            return 1
        if dp[n]!=-1:
            return dp[n]
        ans=0
        for i in range(1,n):
            ans1=i*max(n-i,self.solve(n-i,dp))
            ans=max(ans,ans1)
        dp[n]=ans
        return ans
        